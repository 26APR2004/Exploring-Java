class assignment_op
{
    public static void main(String[]args)
    {
        int a=10;
        a+=3;//a=10+3
        System.out.println(a);
        a-=2;//a=13-2
        System.out.println(a);
        a*=4;//a=11*4
        System.out.println(a);
        a/=11;//a=44/11
        System.out.println(a);
    }
}